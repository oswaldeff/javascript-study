// loops
let i = -3;
while (i < 0) {
    console.log(`start value: ${i}`);
    i++;
    console.log(`end value: ${i}`);
}

console.log('======================');

// do while
do {
    console.log(`start value: ${i}`);
    i++;
    console.log(`end value: ${i}`);
} while (i < 0);

console.log('~~~~~~~~~~~~~~~~~~~~~~~');

// for loop
for (i=3; i>0; i--) {
    console.log(i);
}